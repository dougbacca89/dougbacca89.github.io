/*
 * OPERATORS:
 *
 * Operators act on our data. They can compaire variables, be assigned to a variable, or we
 * can use them in arithmetic operations. Operators are classed by what they do and how many
 * operands they require.
 */
 
// 1. Assignment Operators //
/*
 * Assignment Operators assign a value to its left operand based on the value of its
 * right operand.
 */
// List of Assignment Operators
/*
 * =    => Assignment
 * +=   => Addition Assignment
 * -=   => Subtraction Assignment
 * *=   => Multiplication Assignment
 * /=   => Division Assignment
 * %=   => Remainder Assignment
 */

// 2. Arithmetic Operators //
/*
 * Arithmatic Operators take numerical values as their operands and return a single
 * numerical value.
 */
// List of Arithmetic Operators
/*
 * +    => Addition
 * -    => Subtraction
 * *    => Multiplication
 * /    => Division
 * %    => Remainder
 * ++   => Increment
 * --   => Decrement
 */

// 3. Comparison Operators //
/*
 * Comparison Operators compare its operands and return a logical value based on whether the
 * comparison is true.
 */
// List of Comparison Operators
/*
 * ==   => Equal
 * !=   => Not Equal
 * ===  => Strictly Equal
 * !==  => Strickly Not Equal
 * >    => Greater Than
 * <    => Less Than
 * >=   => Greater Than or Equal To
 * <=   => Less Than or Equal To
 */

// 4. Logical Operators //
/*
 * Logical operators are used with boolean values when they are, they
 * return a boolean value.
 */
// List of Logical Operators
/*
 * &&   => AND
 * ||   => OR
 * !    => NOT or Bang Operator
 */

// 5. Unary Operators //
/*
 * Unary Operators are used in an operation with only one operand.
 */

// 6. Ternary Operator //
/*
 * The Ternary Operator is the only operator the takes three operands. This Operator is normaly
 * used as a shortcut for an if statement.
 */
// Ternary Operator Example
/*
 * condition ? expressionForTrue : expressionForFalse
 */